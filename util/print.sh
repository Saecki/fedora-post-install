#!/bin/sh

GREEN="\033[0;33m"
YELLOW="\033[0;33m"
BLUE="\033[0;34m"
MAGENTA="\033[0;35m"
NC="\033[0m"

install_heading() {
    printf "\n"
    printf "${GREEN}╭──────────────────────────────────────────────────────────────────────────────╮${NC}\n"
    printf "${GREEN}│             ___   _   _   ____    _____      _      _       _                │${NC}\n"
    printf "${GREEN}│            |_ _| | \ | | / ___|  |_   _|    / \    | |     | |               │${NC}\n"
    printf "${GREEN}│             | |  |  \| | \___ \    | |     / _ \   | |     | |               │${NC}\n"
    printf "${GREEN}│             | |  | |\  |  ___) |   | |    / ___ \  | |___  | |___            │${NC}\n"
    printf "${GREEN}│            |___| |_| \_| |____/    |_|   /_/   \_\ |_____| |_____|           │${NC}\n"
    printf "${GREEN}│                                                                              │${NC}\n"
    printf "${GREEN}╰──────────────────────────────────────────────────────────────────────────────╯${NC}\n"
    printf "\n"
}

update_heading() {
    printf "\n"
    printf "${GREEN}╭──────────────────────────────────────────────────────────────────────────────╮${NC}\n"
    printf "${GREEN}│                _   _   ____    ____       _      _____   _____               │${NC}\n"
    printf "${GREEN}│               | | | | |  _ \  |  _ \     / \    |_   _| | ____|              │${NC}\n"
    printf "${GREEN}│               | | | | | |_) | | | | |   / _ \     | |   |  _|                │${NC}\n"
    printf "${GREEN}│               | |_| | |  __/  | |_| |  / ___ \    | |   | |___               │${NC}\n"
    printf "${GREEN}│                \___/  |_|     |____/  /_/   \_\   |_|   |_____|              │${NC}\n"
    printf "${GREEN}│                                                                              │${NC}\n"
    printf "${GREEN}╰──────────────────────────────────────────────────────────────────────────────╯${NC}\n"
    printf "\n"
}

heading1() {
    printf "\n"
    printf "${YELLOW}################################################################################${NC}\n"
    printf "${YELLOW}## %-74s ##${NC}\n" "${@}"
    printf "${YELLOW}################################################################################${NC}\n"
}

heading2() {
    printf "\n"
    printf "${MAGENTA}# %s ${NC}\n" "${@}"
    printf "${MAGENTA}================================================================================${NC}\n"
}

heading3() {
    echo -e "\n${BLUE}${@}${NC}"
}
