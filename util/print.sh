#!/bin/sh

YELLOW="\033[0;33m"
MAGENTA="\033[0;35m"
BLUE="\033[0;34m"
NC="\033[0m"

heading1() {
    printf "\n"
    printf "${YELLOW}################################################################################${NC}\n"
    printf "${YELLOW}## %-74s ##${NC}\n" "${@}"
    printf "${YELLOW}################################################################################${NC}\n"
}

heading2() {
    printf "\n"
    printf "${MAGENTA}# %s ${NC}\n" "${@}"
    printf "${MAGENTA}================================================================================${NC}\n"
}

heading3() {
    echo -e "\n${BLUE}${@}${NC}"
}
